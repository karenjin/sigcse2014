		<p><strong>Description:</strong> In Process Oriented Guided Inquiry Learning (POGIL), teams of 3-5 learners work on scripted inquiry activities designed to help them discover concepts and construct their own knowledge. Using assigned team roles and meta-cognition, learners develop process skills and individual responsibility. The instructor serves as a facilitator, not a lecturer. Studies show that POGIL can significantly improve student performance.  In this workshop, participants will be introduced to the theory and practice of POGIL. They will experience POGIL activities, learn core practices, and draft parts of activities. This workshop is intended for anyone interested in teaching and learning approaches that are described as active, constructivist, or discovery-based, including both college and high school faculty.
